public class DataTypes{
    public static void main(String[] args){
        int a = 10;
        float b = 10.5f;
        long c = 234566785L;
        double d = 234567.8948579;
        char e = 'v'; //strings are declared in double quotes but char is declared in single quotes 
        boolean flag = true; //only has 2 values true and false

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(flag);
    }
}